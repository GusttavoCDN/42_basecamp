#include <unistd.h>

void	ft_putchar(char c);
void	ft_print_reverse_alphabet(void);

void	ft_print_reverse_alphabet(void)
{
	int	i;

	i = 'z';
	while (i >= 'a')
	{
		ft_putchar(i);
		i--;
	}
}

void	ft_putchar(char c)
{
	write(STDOUT_FILENO, &c, 1);
}
